# Читаем первую строку с числами n и m
first_line = input().split()
n = int(first_line[0])
m = int(first_line[1])

# Читаем вторую строку с текстом лекции
text = input()

# Создаем множество для хранения различных слов длины m
words = set()

# Перебираем все возможные подстроки длины m
for i in range(n - m + 1):
    # Вырезаем подстроку длины m, начиная с позиции i
    word = text[i:i + m]
    # Добавляем её в множество (дубликаты игнорируются)
    words.add(word)

# Выводим количество различных слов
print(len(words))
